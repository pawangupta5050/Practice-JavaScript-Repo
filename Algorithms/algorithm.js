// What is an Algorith.

// An algorithm is a step-by-step procedure or a set of instructions designed to perform a specific task or solve a particular problem.

// Time and Space Complexity
// Time complexity and space complexity are measures used in computer science to analyze and describe the efficiency and resource
// usage of algorithms. They help us understand how an algorithm's performance scales with the input size.

// Time Complexity 
// Time complexity refers to the amount of time an algorithm takes to complete its execution as a function of the input size.

// Space Complexity
// Space complexity refers to the amount of memory space an algorithm uses as a function of the input size.


// Big O Notation (O): Big O notation provides an upper bound on the growth rate of an algorithm's time or space complexity. 
// It describes the worst-case behavior of an algorithm as the input size increases.
// For example, if an algorithm has a time complexity of O(n), it means the execution time grows linearly with the input size.

// Omega Notation (Ω): Omega notation provides a lower bound on the growth rate of an algorithm's time or space complexity.
// It describes the best-case behavior of an algorithm as the input size increases. 
// For example, if an algorithm has a time complexity of Ω(n), it means the execution time at least grows linearly with the input size.

// Theta Notation (Θ): Theta notation provides a tight bound on the growth rate of an algorithm's time or space complexity. 
// It describes both the upper and lower bounds of an algorithm's behavior.
// For example, if an algorithm has a time complexity of Θ(n), it means the execution time grows linearly with the input size, 
// and the upper and lower bounds match.